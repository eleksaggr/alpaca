pub struct Keyboard {}

impl Keyboard {
    pub fn convert(scan: u16) -> char {
        match scan {
            0x82 => '1',
            0x83 => '2',
            0x84 => '3',
            0x85 => '4',
            0x86 => '5',
            0x87 => '6',
            0x88 => '7',
            0x89 => '8',
            0x8A => '9',
            0x8B => '0',
            0x8C => '-',
            0x8D => '=',
            0x90 => 'q',
            0x91 => 'w',
            0x92 => 'e',
            0x93 => 'r',
            0x94 => 't',
            0x95 => 'y',
            0x96 => 'u',
            0x97 => 'i',
            0x98 => 'o',
            0x99 => 'p',
            0x9a => '[',
            0x9b => ']',
            0x9c => '\n',
            0x9e => 'a',
            0x9f => 's',
            0xa0 => 'd',
            0xa1 => 'f',
            0xa2 => 'g',
            0xa3 => 'h',
            0xa4 => 'j',
            0xa5 => 'k',
            0xa6 => 'l',
            0xa7 => ';',
            0xa8 => '\'',
            0xa9 => '`',
            0xab => '\\',
            0xac => 'z',
            0xad => 'x',
            0xae => 'c',
            0xaf => 'v',
            0xb0 => 'b',
            0xb1 => 'n',
            0xb2 => 'm',
            0xb3 => ',',
            0xb4 => '.',
            0xb5 => '/',
            0xb9 => ' ',
            _ => '@',
        }
    }
}
